#and (and will return true if all the statements are true else it will return false)
#or  (or will return true if any one statement is true else it will return false)
#not (not it will return true when statement is false .if the statement is true it will return false)
print("and operator (and):=============================================================================")
print("(5>3 and 5!=4 and 5==5:",5>3 and 5!=4 and 5==5)#here all the statements are correct so it return true
print("15>13 and 17>5:",15>13 and 17>5) #here second statement is false so it will reurn false
print("or operator (or):===============================================================================")
print("5>3 or 7>4 or 6>3:",5>3 or 7>4 or 6>3) #any one statement is true .then it will return true
print("(5>7 and 6==6) or (7!=6 and 2<7):",(5>7 and 6==6) or (7!=6 and 2<7)) #or with and operator
print("not operator (not):=============================================================================")
print("not 10==10:",not 10==10) #the statement is true .it will return opposite value so result is false
print("not(5>3 and 10>17):",not(5>3 and 10>17)) #not with and opertor
print("not((5>7 and 6==6) or (7!=6 and 2<7)):",not((5>7 and 6==6) or (7!=6 and 2<7)))#not operator with "and" and "or" operator 

